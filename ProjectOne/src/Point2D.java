
public class Point2D {
	private double pointx = 0;
	private double pointy = 0;
	public Point2D() {
	}
	public double getPointx() {
		return pointx;
	}
	public void setPointx(double pointx) {
		this.pointx = pointx;
	}
	public double getPointy() {
		return pointy;
	}
	public void setPointy(double pointy) {
		this.pointy = pointy;
	}

	
	
}
